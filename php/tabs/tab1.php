<fieldset class="infoList"><legend class="font_legend"><b>Description de l’outil</b></legend>

		Cet outil est avant tout un outil de communication</b>, l’objectif est de comprendre et évaluer les liens de votre organisation vis-à-vis de la biodiversité et de communiquer à ce propos avec les parties prenantes de l’organisation. Cette approche utilise le référentiel des Services Écosystémiques (SE) qui aborde les questions de biodiversité d’un point de vue anthropique, ce qui rend l’exercice accessible à tous. **
		<br>
		<li>Les étapes de l’évaluation sont les suivantes :</li>
